package c00_Intro;

public class DataTypes {

	public static void main(String[] args) {
		
		boolean value = false;
		boolean value2 = true;
		System.out.println("Boolean false value = " + value);
		System.out.println("Boolean true value = " + value2);
		
		byte one = 1;
		System.out.println("Byte variable value = " + one);
		
		int a = 1185;
		System.out.println("int variable value = " + a);
		
		long longValue = 2260118511602285L;
		System.out.println("long variable value = " + longValue);
		
		double data= 11.22;
		System.out.println("double variable value = " + data);
		
		float val = 22.60f;
		System.out.println("float variable value = " + val);
		
		char c = 'c';
		System.out.println("value of char variable c = " + c);
	}
}
