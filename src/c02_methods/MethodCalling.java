package c02_methods;

public class MethodCalling {

	public static void main(String[] args) {
		
		//calling printHello method
		printHello();
		
		//calling printHai method
		printHai();
		
		//calling printCodingMaxima method
		printCodingMaxima();	
	}

	public static void printCodingMaxima() {
		
		System.out.println("Print Coding Maxima Method Executed");	
	}

	public static void printHai() {
		
		System.out.println("Print Hai Method Executed");	
	}

	public static void printHello() {
		
		System.out.println("Print Hello Method Executed");	
	}
}
